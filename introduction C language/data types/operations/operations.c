// working with operations in C language

#include <stdio.h>

int main()
{

    // variables
    int num1, num2, result;
    num1 = 10;
    num2 = 5;

    //multiplication
    result = num1 * num2;   // result = 50

    // addition
    result = num1 + num2;   // result = 15

    // subtraction
    result = num1 - num2;   // result = 5

    // division
    result = num2 / num1;   // result = 2

    // module
    result = num1 % num2;   // result = 0

    // operator increment
    result++;               // result = 1

    // operator decrement
    result--;               // result = 0;

    return 0;
}
